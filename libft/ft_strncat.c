#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*pnt;
	char	*src;

	pnt = s1;
	src = (char *)s2;
	while (*pnt)
		pnt++;
	while (*src && n--)
		*pnt++ = *src++;
	*pnt = '\0';
	return (s1);
}
